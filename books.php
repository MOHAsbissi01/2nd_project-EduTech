                            <h3>${title}</h3>
                            <p>Author(s): ${authors}</p>
                            <p>${description}</p>
                            
                        </div>
                    `;

                    resultsDiv.innerHTML += bookCard;
                });
            } catch (error) {
                console.error('Error fetching data:', error);
            }
